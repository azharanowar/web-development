// Area of rectangle by width and length program 

function areaOfRectangle ( length, width ) {
    const result = length * width;

    return result;
}


const result1 = areaOfRectangle( 10, 10 );
const result2 = areaOfRectangle( 100, 50 );
const result3 = areaOfRectangle( 50, 20 );

console.log( result1, result2, result3 );